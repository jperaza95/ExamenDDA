package figuras;

/**
 *
 * @author julio
 */
public abstract class Figura {
    private int id;
    private String color;

    public Figura(int id, String color) {
        this.id = id;
        this.color = color;
    }

    public int getId() {
        return id;
    }

    public String getColor() {
        return color;
    }
   
    public abstract String dibujar();
    public abstract int area();
    
    
}
