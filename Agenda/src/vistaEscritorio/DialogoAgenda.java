    private void crearContacto() {
        String nom = tfNombre.getText();
        String tel = tfTelefono.getText();
        TipoContacto tc = (TipoContacto)comboTipoContacto.getSelectedItem();
        TipoTelefono tf = (TipoTelefono)comboTipoTelefono.getSelectedItem();
        
        controlador.crearContacto(nom,tel,tc,tf); 
    }





    private void logout() {
        controlador.logout();

    }
    
    @Override
    public void buscar(){
        dialogoB.setVisible(true);
    }
    

    @Override
    
    public void mostrarTiposContacto(ArrayList<TipoContacto> tiposContacto) {
        comboTipoContacto.removeAllItems();
        for(TipoContacto tc : tiposContacto){
            comboTipoContacto.addItem(tc);
        }
    }
    
    @Override
    public void mostrarTiposTelefono(ArrayList<TipoTelefono> tiposTelefono) {
        comboTipoTelefono.removeAllItems();
        for(TipoTelefono tt : tiposTelefono){
            comboTipoTelefono.addItem(tt);
        }
    }
    
    private void setearTitulo(UsuarioAgenda u) {
        setTitle(u.getNombreCompleto().toUpperCase()+" - Contactos: "+u.getAgenda().cantidadContactos());

    }
    
    @Override
    
    public void mostrarEstado(UsuarioAgenda u) {
        limpiarCampos();
        setearTitulo(u);
        listContactos.setListData(u.getAgenda().getContactos().toArray());
        dialogoB.setTitle("Busqueda en la agenda de "+u.getNombreCompleto());
        dialogoB.buscar();
    }    
    
    @Override
    
    public void mostrarMensaje(String msg){
            JOptionPane.showMessageDialog(this, msg);
    
    }
    


    @Override
    public void mostrarResultadoBusqueda(ArrayList<Contacto> lista) {
        dialogoB.mostrarResultadoBusqueda(lista);
    }

    @Override
    public void mostrarDetalles(Date fechaCreacion, String nombre, TipoContacto tipoContacto, Telefono telefono) {
       dialogoB.mostrarDetalles(fechaCreacion,nombre,tipoContacto,telefono);
    }

    private void editar() {
        Contacto c = (Contacto)listContactos.getSelectedValue();
        if(c!=null) new DialogoModificarContacto((Frame)getParent(), true, c).setVisible(true);
    }

    private void limpiarCampos() {
        tfNombre.setText("");
        tfTelefono.setText("");
    }




