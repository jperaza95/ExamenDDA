    private void salir() {
       int opcion = JOptionPane.showConfirmDialog(this, "Confirmar salida");
        if (opcion == JOptionPane.YES_OPTION) {
            System.exit(0); // termina la ejecuci√≥n de la JVM
        }
    }

    private void nuevoDialogo() {
        Dialogo d = new Dialogo(this, true,"DIALOGO "+ contador);
        contador++;
        d.setVisible(true);
        dialogos.add(d);
        mostrarDialogos();
        
    }

    public void mostrarDialogos() {
       comboVentanas.removeAllItems();
       for(Dialogo d: dialogos){
           comboVentanas.addItem(d);
       }
       
    }

    private void activarDialogo() {
        Dialogo d = (Dialogo)comboVentanas.getSelectedItem();
        if (d!=null) d.toFront();
    }

    public void cierre(Dialogo unDialogo) {
        dialogos.remove(unDialogo);
        mostrarDialogos();
    }

    private void cerrarSeleccionado() {
        Dialogo d = (Dialogo)comboVentanas.getSelectedItem();
        
        if (d!=null) {
            d.dispose(); //que se termine
            cierre(d);
        }
        
    }
